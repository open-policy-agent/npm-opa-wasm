plus = (a, b) => a + b;
minus = (a, b) => a - b;
mul = (a, b) => a * b;
div = (a, b) => a / b;
rem = (a, b) => a % b;
round = (x) => Math.round(x);
ceil = (x) => Math.ceil(x);
floor = (x) => Math.floor(x);
abs = (x) => Math.abs(x);

module.exports = { plus, minus, mul, div, rem, round, ceil, floor, abs };
